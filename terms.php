<script>
document.addEventListener('DOMContentLoaded', function() {
    // Smooth scrolling for navigation links
    const navLinks = document.querySelectorAll('.terms-nav .nav-link');
    
    navLinks.forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            const targetElement = document.querySelector(targetId);
            
            if (targetElement) {
                const headerHeight = document.querySelector('.header').offsetHeight;
                const targetPosition = targetElement.offsetTop - headerHeight - 20;
                
                window.scrollTo({
                    top: targetPosition,
                    behavior: 'smooth'
                });
                
                // Update active link
                navLinks.forEach(l => l.classList.remove('active'));
                this.classList.add('active');
            }
        });
    });
    
    // Highlight current section on scroll
    window.addEventListener('scroll', function() {
        const articles = document.querySelectorAll('.terms-article');
        const headerHeight = document.querySelector('.header').offsetHeight;
        
        articles.forEach(article => {
            const rect = article.getBoundingClientRect();
            const articleId = '#' + article.id;
            const navLink = document.querySelector(`.terms-nav .nav-link[href="${articleId}"]`);
            
            if (rect.top <= headerHeight + 50 && rect.bottom >= headerHeight + 50) {
                navLinks.forEach(l => l.classList.remove('active'));
                if (navLink) {
                    navLink.classList.add('active');
                }
            }
        });
    });
});
</script>

